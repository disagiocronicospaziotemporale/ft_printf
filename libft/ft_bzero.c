#include "libft.h"

void				ft_bzero(void *s, int n)
{
	int				i;
	unsigned char	*s_copy;

	s_copy = (unsigned char*)s;
	i = 0;
	while (i < n)
	{
		s_copy[i] = 0;
		i++;
	}
}
